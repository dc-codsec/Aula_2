
#Ja conhecemos o operador de atribuição = 
ssd = 500
print(ssd)
ssd = ssd + 500
print(ssd)

#Ao atribuir o valor da variavel a ela mesmo 
#efetuando outra operação, o nome da variavel 
#pode ser omitido e o operador trocador por
# += -= *= /= //= %= a depender da operação
ssd += 500 # mesma coisa ssd = ssd + 500
print(ssd)

ssd = ssd - 500
print(ssd)

ssd %= 500 # mesma coisa ssd = ssd / 500
print(ssd)

