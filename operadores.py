
#Operadores permitem efetuarmos operações matemáticas,
#atribuições e comparações.

#soma + tem duas funções: concatenar str e somar int/float
memoria_slot1 = "1024"
memoria_slot2 = 1024
print(memoria_slot1 + memoria_slot1)
print(memoria_slot2 + memoria_slot2)

#multiplicacao *  tem duas funções: repetir str e multiplicar int/float
print(memoria_slot1 * 3)
print(memoria_slot2 * 3)

#divisao /
print(9 / 2)

#divisao inteira //
print(9 // 2.3) #retorna a parte inteira 

#módulo % resto da divisão
print(9 % 2)

