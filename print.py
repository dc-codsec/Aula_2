"""
Funções são codigos que executam uma ação especifica
Em python uma identificamos uma função com uma palavra
seguida de parenteses: funcao()
Podem ou não receber parâmetros
"""
print("Função print()")
"""
Função print, pode receber varios objetos str
separados por virgula. 
Por padrão serão separados por espaço 
e terminados com o cursor em uma nova linha
"""
print("String 1", "String 2")

"""
Esses comportamentos podem ser alterados 
através dos parâmetros sep="" para alterar o 
caracter do espaço e end="\n"
para o final 
"""

print("coding", "for",sep="#", end="\t")
